let time = "6:30"
switch(time){
    case"7:30":
        console.log("It's early in the morning")
        break
    case"12:30":
        console.log("It's mid-day")
        break
    case"16:30":
        console.log("It's time for dinner")
        break
    case"22:30":
        console.log("It's time to go to bed")
        break
    case"6:30":
        console.log("It's time to wake up")
        break
    default:
        console.log("No condition met")
}
    